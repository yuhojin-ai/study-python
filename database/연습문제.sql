-- 01번: SQL에 대한 설명
-- ① SQL은 구조화된 질의 언어이다.            -- O
-- ② NULL은 0 또는 공백을 의미한다.            -- X (NULL은 "값 없음", 0/공백과 다름)
-- ③ 테이블은 행과 열로 구성된다.              -- O
-- ④ 기본키로 NULL이나 중복되는 값을 입력할 수 있다. -- X (기본키는 NULL 불가, 중복 불가)

-- 02번: 두 테이블을 수직 병합 (중복 제거)
-- UNION: 수직 방향으로 합치기 (중복 제거)
SELECT 이름 FROM 학생
UNION
SELECT 이름 FROM 수강;

-- 03번: 여러 값을 조건으로 검색할 때
SELECT * FROM 학생
WHERE 학번 IN (1, 2, 3);  -- IN은 여러 값 중 일치하는 경우 선택

-- 04번: 과목별 수강인원 구하기
SELECT 과목번호, COUNT(*) AS 수강인원
FROM 수강
GROUP BY 과목번호;  -- GROUP BY로 과목번호별로 묶고 COUNT로 인원수 계산

-- 05번: 수강 테이블에서 중복 없이 과목번호 조회
SELECT DISTINCT 과목번호
FROM 수강;  -- DISTINCT로 중복 제거

-- 06번: s3 학생의 전화번호 수정
UPDATE 학생
SET 전화번호 = '010-1234-5678'
WHERE 학번 = 'S3';  -- WHERE로 특정 학생 지정, SET으로 값 수정

-- 07번: s3 학생의 데이터 삭제
DELETE FROM 학생
WHERE 학번 = 'S3';  -- 특정 조건 만족하는 행만 삭제

-- 08번: 주소가 '대전' 또는 '서울'이고, 2000년에 태어난 학생 정보
SELECT 이름, 주소, 생년월일
FROM 학생
WHERE (주소 LIKE '대전%' OR 주소 LIKE '서울%')  -- 대전 또는 서울 포함 주소
  AND 생년월일 BETWEEN '2000-01-01' AND '2000-12-31';  -- 2000년 출생자

-- 09번: C1 과목 수강 중인 학생 수 출력
SELECT COUNT(*) AS "학생 수"
FROM 수강
WHERE 과목번호 = 'C1';  -- 조건 만족하는 행 수를 COUNT

-- 10번: 이름에 '신'이 들어간 학생 검색
SELECT 학번, 이름
FROM 학생
WHERE 이름 LIKE '%신%';  -- %는 와일드카드, '신'이 포함된 모든 이름 검색